// ---------------------------------------------------------------
//  Display uncompressed image - SIMPLE VERSION
// ---------------------------------------------------------------
void displayImage(int x0, int y0, int imgW = 64, int imgH = 64) {
    for (int y = 0; y < imgH; y++) {
        for (int x = 0; x < imgW; x++) {
            // Read palette index from PROGMEM
            uint8_t idx = pgm_read_byte(&image_data[y * imgW + x]);
            
            // Look up color in palette
            uint16_t color = pgm_read_word(&palette[idx]);
            
            // Draw pixel
            dma_display->drawPixel(x0 + x, y0 + y, color);
        }
    }
}

// ---------------------------------------------------------------
//  Display uncompressed image - OPTIMIZED VERSION (faster)
// ---------------------------------------------------------------
void displayImageFast(int x0, int y0, int imgW = 64, int imgH = 64) {
    for (int y = 0; y < imgH; y++) {
        // Process entire row
        int rowOffset = y * imgW;
        
        for (int x = 0; x < imgW; x++) {
            uint8_t idx = pgm_read_byte(&image_data[rowOffset + x]);
            uint16_t color = pgm_read_word(&palette[idx]);
            dma_display->drawPixel(x0 + x, y0 + y, color);
        }
    }
}
  
